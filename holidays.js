/**
 * Returns true if the given session is fully closed for that date.
 * @param {string} sessionKey - 'sydney' | 'tokyo' | 'london' | 'newyork'
 * @param {string} dateString - 'YYYY-MM-DD'
 */
export function isHoliday(sessionKey, dateString) {
  const sessionHolidays = holidays[sessionKey];
  if (!sessionHolidays) return false;
  return Object.prototype.hasOwnProperty.call(sessionHolidays, dateString);
}

/**
 * Returns the holiday name for a given session/date, or null.
 */
export function getHolidayName(sessionKey, dateString) {
  const sessionHolidays = holidays[sessionKey];
  if (!sessionHolidays) return null;
  return sessionHolidays[dateString] || null;
}

/**
 * Returns true if this UTC date is on the session's weekend (no trading).
 *
 * For non-overnight sessions (Tokyo/London/NY): closed on Sat & Sun UTC.
 * For Sydney (overnight, opens 22:00 UTC, trading day = next UTC day):
 *   - opening on Fri UTC → trading day = Sat (Sydney weekend)
 *   - opening on Sat UTC → trading day = Sun (Sydney weekend)
 *   So Sydney is "weekend" when the UTC date is Fri or Sat.
 *   Sun UTC 22:00 opens Mon's trading day → not weekend.
 */
export function isSessionWeekend(sessionKey, dateString) {
  // Parse YYYY-MM-DD as UTC midnight to get correct day-of-week
  const [y, m, d] = dateString.split('-').map(Number);
  const dow = new Date(Date.UTC(y, m - 1, d)).getUTCDay(); // 0=Sun..6=Sat

  if (sessionKey === 'sydney') {
    return dow === 5 || dow === 6; // Fri or Sat
  }
  return dow === 0 || dow === 6;   // Sun or Sat
}

/**
 * Returns the adjusted UTC open/close hours for a given session on a given date.
 * If holiday or weekend → returns closedAllDay: true.
 * If early-close → returns adjusted close.
 */
export function getSessionHours(sessionKey, dateString) {
  const defaults = {
    sydney:  { open: 22, close: 7 },
    tokyo:   { open: 0,  close: 9 },
    london:  { open: 8,  close: 17 },
    newyork: { open: 13, close: 22 }
  };

  const def = defaults[sessionKey];
  if (!def) return null;

  if (isHoliday(sessionKey, dateString)) {
    return {
      open: def.open,
      close: def.close,
      holiday: getHolidayName(sessionKey, dateString),
      weekend: false,
      earlyClose: false,
      closedAllDay: true
    };
  }

  if (isSessionWeekend(sessionKey, dateString)) {
    return {
      open: def.open,
      close: def.close,
      holiday: null,
      weekend: true,
      earlyClose: false,
      closedAllDay: true
    };
  }

  const earlyMap = earlyCloseMap[sessionKey] || {};
  if (Object.prototype.hasOwnProperty.call(earlyMap, dateString)) {
    return {
      open: def.open,
      close: earlyMap[dateString],
      holiday: null,
      weekend: false,
      earlyClose: true,
      closedAllDay: false
    };
  }

  return {
    open: def.open,
    close: def.close,
    holiday: null,
    weekend: false,
    earlyClose: false,
    closedAllDay: false
  };
}

/**
 * Format a Date as a UTC YYYY-MM-DD string.
 */
export function toUTCDateString(date) {
  const y = date.getUTCFullYear();
  const m = String(date.getUTCMonth() + 1).padStart(2, '0');
  const d = String(date.getUTCDate()).padStart(2, '0');
  return `${y}-${m}-${d}`;
}
